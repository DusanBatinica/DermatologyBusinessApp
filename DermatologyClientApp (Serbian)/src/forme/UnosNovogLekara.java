    private void validacijaIPostavkaPodataka(String ime, String prezime, String kontakt) throws ValidacijaException {
        if (ime.isEmpty() || prezime.isEmpty() || kontakt.isEmpty()) {
            throw new ValidacijaException("Sva polja su obavezna!");

        }
        lekar.setIme(ime);
        lekar.setPrezime(prezime);
        lekar.setKontakt(kontakt);
    }
