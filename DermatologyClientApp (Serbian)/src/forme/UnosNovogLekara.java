    private void btnSacuvajLekaraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajLekaraActionPerformed
        String lekarID = txtLekarID.getText();
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String kontakt = txtKontakt.getText();

        try {
            validacijaIPostavkaPodataka(ime, prezime, kontakt);
        } catch (ValidacijaException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Greška prilikom validacije", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Lekar l;
        try {
            l = Kontroler.vratiInstancu().sacuvajLekara(lekar);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio novog lekara:\nID: " + l.getLekarID() + "\nIme lekara: " +l.getIme()+ "\nPrezime: " + l.getPrezime()+ "\nKontakt: " + l.getKontakt(), "Uspešno", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception ex) {
            Logger.getLogger(UnosNovogLekara.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajLekaraActionPerformed

